
package trab03;


public class Editora {
    int idEditora;
    String nome;

    public Editora(int idEditora, String nome) {
        this.idEditora = idEditora;
        this.nome = nome;
    }

    public Editora() {
    }

    public int getIdEditora() {
        return idEditora;
    }

    public void setIdEditora(int idEditora) {
        this.idEditora = idEditora;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }


    
    
}
